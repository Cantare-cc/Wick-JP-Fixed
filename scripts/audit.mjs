const NUMERIC = /^[0-9.,$%kKmM\s]+$/;
const PUNCT_ONLY = /^[\s.,;:!?'"@#$%^&*()\-=+/\\|\[\]{}<>]+$/;
function isContent(s) {
  if (!s) return false;
  const t = s.trim();
  if (!t) return false;
  if (NUMERIC.test(t)) return false;
  if (PUNCT_ONLY.test(t)) return false;
  if (!/[A-Za-z]/.test(t)) return false;
  return true;
}


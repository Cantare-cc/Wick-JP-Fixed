const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

function makeTranslator(hostname) {
  const dictJs = readFileSync(join(root, "build/chrome/dictionary.js"), "utf8");
  const trJs = readFileSync(join(root, "build/chrome/translations.js"), "utf8");
  const ctx = { self: { location: { hostname } }, console };
  vm.createContext(ctx);
  vm.runInContext(dictJs.replace(/typeof self !== "undefined"/g, "true"), ctx);
  vm.runInContext(
    trJs
      .replace(/typeof self !== "undefined"/g, "true")
      .replace(/location\.hostname/g, "self.location.hostname"),
    ctx
  );
  return ctx.self.WickJpTranslations;
}

